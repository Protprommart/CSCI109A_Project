IMPLAUSIBLE_THRESHOLDS = {
    "serum_sodium": {
        "min": 100,
        "max": 190
    },
    "serum_creatinine": {
        "min": 0.2
         #no max; see above
    },
    "platelets": {
        "min": 5000,
        "max": 2_000_000
    },
    "ejection_fraction": {
        "min": 0,
        "max": 90
    },
    "creatinine_phosphokinase": {
        "min": 1,
        "max": 250_000
    }
}

def check_row_implausible(row, thresholds=IMPLAUSIBLE_THRESHOLDS):

    issues = {}

    for lab, bounds in thresholds.items():
        if lab not in row or pd.isna(row[lab]):
            continue

        val = row[lab]

        if "min" in bounds and val < bounds["min"]:
            issues[lab] = f"{val} < {bounds['min']}"

        if "max" in bounds and val > bounds["max"]:
            issues[lab] = f"{val} > {bounds['max']}"

    return issues if issues else None

def split_implausible_rows(df, thresholds=IMPLAUSIBLE_THRESHOLDS):

    df_copy = df.copy()

    implausible_info = []

    # loop through the df
    for idx, row in df_copy.iterrows():
        issues = check_row_implausible(row, thresholds)
        implausible_info.append(issues)

    mask_implausible = pd.Series(
      [i is not None for i in implausible_info],
      index=df_copy.index
      )

    # Split df
    df_removed = df_copy[mask_implausible].copy()
    df_clean   = df_copy[~mask_implausible].copy()

    df_removed["implausible_issues"] = [i for i in implausible_info if i is not None]

    summary = {}
    for issues in df_removed["implausible_issues"]:
        for lab in issues:
            summary[lab] = summary.get(lab, 0) + 1

    return df_clean.reset_index(drop=True), df_removed.reset_index(drop=True), summary

def print_implausible_summary(summary):
  if not summary:
      print("No implausible values found.")
      return

  print("Implausible value summary:")
  for lab, count in summary.items():
      print(f"  {lab}: {count} rows flagged")