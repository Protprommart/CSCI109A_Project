def unreg_logreg(X_train, y_train, X_test, y_test):
  cv_folds = 10
  random_state = 42
  kf = KFold(n_splits=cv_folds, shuffle=True, random_state = random_state)

  pipe = Pipeline([
    ('scaler', StandardScaler()),
     ('logreg', LogisticRegression(penalty=None, random_state=random_state,
                                  max_iter = 3500))])
  scoring = ['accuracy', 'precision', 'recall', 'f1', 'roc_auc']
  lr_cv_scores = cross_validate(
      pipe,
      X_train,
      y_train,
      cv=kf,
      scoring=scoring,
      n_jobs=-1,
      return_train_score=True
  )
  print('Cross-validation scores:')
  print(f"Cross-validation accuracy: {lr_cv_scores['test_accuracy'].mean():.4f}")
  print(f"Cross-validation precision: {lr_cv_scores['test_precision'].mean():.4f}")
  print(f"Cross-validation recall: {lr_cv_scores['test_recall'].mean():.4f}")
  print(f"Cross-validation f1: {lr_cv_scores['test_f1'].mean():.4f}")
  print(f"Cross-validation roc_auc: {lr_cv_scores['test_roc_auc'].mean():.4f}")
  print('------------------------------------------------------------------------')

  pipe.fit(X_train, y_train) #refit model on all training data before proceeding to final model
  lr = pipe.named_steps['logreg']

  unreg_coef_df = pd.DataFrame({
    'Feature': X_train.columns,
    'Coefficient': lr.coef_[0],
    'Odds_Ratio': np.exp(lr.coef_[0])})
  unreg_coef_df = unreg_coef_df.sort_values('Coefficient', ascending=False)

  print("Unregularized Model Coefficients:")
  print(unreg_coef_df)
  print("\nIntercept:", lr.intercept_[0])
  print('------------------------------------------------------------------------')

  y_pred = pipe.predict(X_test)
  y_pred_proba = pipe.predict_proba(X_test)[:, 1]

  test_accuracy = accuracy_score(y_test, y_pred)
  test_precision = precision_score(y_test, y_pred)
  test_recall = recall_score(y_test, y_pred)
  test_f1 = f1_score(y_test, y_pred)
  test_roc_auc = roc_auc_score(y_test, y_pred_proba)

  print('Test scores:')
  print(f"Test Accuracy:  {test_accuracy:.4f}")
  print(f"Test Precision: {test_precision:.4f}")
  print(f"Test Recall:    {test_recall:.4f}")
  print(f"Test F1:        {test_f1:.4f}")
  print(f"Test ROC AUC:   {test_roc_auc:.4f}")

  return pipe, lr_cv_scores, lr, unreg_coef_df, test_accuracy, test_precision, test_recall, test_f1, test_roc_auc

