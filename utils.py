def polynomial_logreg(X_train, y_train, X_test, y_test):
  cv_folds = 10
  random_state = 42
  max_iter = 1000
  poly_lasso_pipeline = Pipeline([
      ('poly', PolynomialFeatures(degree=3, include_bias=False)),
      ('scaler', StandardScaler()),
      ('lr', LogisticRegression(
          penalty='l1',
          solver='liblinear',
          max_iter=max_iter,
          random_state=random_state
      ))
  ])

  param_grid = {'lr__C': np.logspace(-3, 3, 20)}

  scoring = {
      'accuracy': 'accuracy',
      'precision': 'precision',
      'recall': 'recall',
      'f1': 'f1',
      'roc_auc': 'roc_auc'
  }

  kf = KFold(n_splits=10, shuffle=True, random_state=42)

  best_polyreg_lr = GridSearchCV(
      estimator=poly_lasso_pipeline,
      param_grid=param_grid,
      scoring=scoring,
      cv=kf,
      refit='f1',
      n_jobs=-1,
      return_train_score=False
  )

  best_polyreg_lr.fit(X_train, y_train)

  print('Cross-validation results:')
  print(f"Best C (optimized for F1): {best_polyreg_lr.best_params_['lr__C']:.4f}")
  print("\nBest CV metrics (mean across folds):")
  mean_scores = {
      metric: best_polyreg_lr.cv_results_[f'mean_test_{metric}'][best_polyreg_lr.best_index_]
      for metric in scoring.keys()
  }

  for metric, score in mean_scores.items():
      print(f"{metric}: {score:.4f}")

  feature_names = best_polyreg_lr.best_estimator_.named_steps['poly'].get_feature_names_out(X_train.columns)

  final_lr = best_polyreg_lr.best_estimator_.named_steps['lr']

  coefficients = final_lr.coef_[0]

  coef_df = pd.DataFrame({
      'Feature': feature_names,
      'Coefficient': coefficients,
      'Abs_Coefficient': np.abs(coefficients)
  })

  coef_df = coef_df.sort_values('Abs_Coefficient', ascending=False)

  print('------------------------------------------------------------------------')

  print("\nTop non-zero coefficients:")
  print(coef_df[coef_df['Coefficient'] != 0].head(20).to_string())

  return best_polyreg_lr, best_polyreg_lr, final_lr, coef_df

